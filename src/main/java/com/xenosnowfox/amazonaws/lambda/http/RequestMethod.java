package com.xenosnowfox.amazonaws.lambda.http;

/**
 * Enumeration of possible Request Methods.
 *
 * @since
 *      1.0.0
 */
public enum RequestMethod {
    /**
     * GET Request.
     */
    GET

    /**
     * POST Request.
     */
    , POST

    /**
     * PUT Request.
     */
    , PUT

    /**
     * PATCH Request.
     */
    , PATCH

    /**
     * DELETE Request.
     */
    , DELETE

    /**
     * HEAD Request.
     */
    , HEAD

    /**
     * OPTIONS Request.
     */
    , OPTIONS
}
